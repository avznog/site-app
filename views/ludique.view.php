			<h1 class="titre1">Le produit Take'R</h1>
			<p class="texte">La société Infinite Measures propose à ses entreprises clientes un système de mesure de la qualité environnementale de leurs employés.</p>
			<p class="texte">En effet, l'espace de travail au sein d'une entreprise nuit parfois à la santé de ses salariés. Dans l'objectif d'offrir au plus grand nombre une meilleure qualité de vie, notre société vous propose le produit Take'R.</p>
			<p class="texte">Le produit Take'R est composé de plusieurs cinq capteurs :</p>
			<h3 class="titre3">Capteur de température</h3>
			<p class="texte">Dans un espace de travail, la température idéale s'étend de 20°C à 26°C.</p>
			<h3 class="titre3">Capteur d'humidité</h3>
			<p class="texte">Le taux d'humidité est également important pour la santé des employés se situe entre 40% et 60%.</p>
			<h3 class="titre3">Capteur sonore</h3>
			<p class="texte">Pour assurer une bonne concentration, il est important de travailler dans un endroit silencieux. Aussi, ce capteur permet de vérifier que l'intensité sonore est inférieure à 50 dB.</p>
			<h3 class="titre3">Capteur du taux de CO2</h3>
			<p class="texte">Un important taux de CO2 dans un espace fermé est néfaste pour les employés. L'INRS (Institut Nationale de Recherche et de Sécuité) recommande de fixer un objectif de concentration en CO2 de 1000 ppm.</p>
			<h3 class="titre3">Capteur cardiaque</h3>
			<p class="texte">Afin d'éviter un maximum de stress au travail, nous avons ajouté un capteur cardiaque. Au repos, le rythme cardiaque se situe entre 50 bpm et 90 bpm.</p>
			
			<h1 class="titre1">L'Application Ludique</h1>
			<p class="texte">A travers le projet Take'R, la société Infinite Measures a pour objectif de sensibiliser et de familiariser les gens à la qualité de leur environnement de travail. En donnant à chaque employé un produit Take'R à poser sur leur bureau, les possésseurs du produit ont accès à chacune de leurs données mesurées dans leur espace personnel sur notre site. Ils peuvent alors réalisé la qualité de leur espace de travail et ainsi devenir acteur de leur environnement, par exemple en ouvrant une fenêtre, ou en adoptant une jolie plante verte sur leur bureau.</p>
			<p class="texte">De manière à intéresser le plus grand nombre, nous avons ajouté un système de points. Chaque mois et pour chaque capteur, une moyenne des données mesurées au cour de ce mois est réalisée. Chaque capteur possède une plage de données cibles : </p>
			<ul><li class="liste">Capteur de Température : entre 20°C et 26°C</li>
				<li class="liste">Capteur d'Humidité : entre 40% et 60%</li>
				<li class="liste">Capteur Sonore : inférieur à 50 dB</li>
				<li class="liste">Capteur du taux de CO2 : inférieur à 1000 ppm</li>
				<li class="liste">Capteur Cardiaque : entre 50 bpm et 90 bpm</li>
			</ul><br/>
			<p class="texte">Si la moyenne obtenue pour un capteur appartient à l'intervalle cible, l'employé gagne 10 points. Si, à la fin d'un mois, l'environnement de travail d'un employé est jugé idéal (la moyenne de chacun des capteurs appartient à la plage ciblée), l'employé gagne un bonus de 5 points. En revanche, si la moyenne obtenue pour un capteur n'appartient pas à son intervalle cible, l'employé perd 2 points. Il perd 5 points supplémentaires si son environnement de travail est jugé dangeureux (si la moyenne d'aucun des capteurs appartient à la plage ciblée). Un employé peut retrouver son nombre de points dans son espace personnel sur notre site.</p>

			<p class="texte">A partir de 100 points, l'employé peut demander à convertir ces points en bon d'achat pour des entreprises telles que la Fnac, Darty, Maison du Monde, Nature et Découverte...<br/>
			La conversion se fait de la manière suivante : 10 points = 1 €.</p>
